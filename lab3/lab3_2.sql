DROP TABLE IF EXISTS Physician CASCADE;
DROP TABLE IF EXISTS Patient CASCADE;
CREATE TABLE Patient (
  PatientNo BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  DOB DATE NOT NULL,
  EmergencyContactName TEXT,
  EmergencyContactPhone TEXT
);
CREATE TABLE PatientAddress (
  AddressID BIGSERIAL PRIMARY KEY,
  PatientNo BIGINT NOT NULL REFERENCES Patient(PatientNo) ON DELETE CASCADE,
  AddressLine TEXT NOT NULL,
  City TEXT NOT NULL,
  Region TEXT,
  Zip TEXT
);
CREATE TABLE PatientPhone (
  PhoneID BIGSERIAL PRIMARY KEY,
  PatientNo BIGINT NOT NULL REFERENCES Patient(PatientNo) ON DELETE CASCADE,
  Phone TEXT NOT NULL,
  Type TEXT
);
CREATE TABLE InsurancePolicy (
  PolicyID BIGSERIAL PRIMARY KEY,
  PatientNo BIGINT NOT NULL REFERENCES Patient(PatientNo) ON DELETE CASCADE,
  Provider TEXT NOT NULL,
  PolicyNumber TEXT NOT NULL,
  UNIQUE (Provider, PolicyNumber)
);
CREATE TABLE Physician (
  PhysicianID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  StartDate DATE NOT NULL
);
CREATE TABLE PhysicianPhone (
  PhoneID BIGSERIAL PRIMARY KEY,
  PhysicianID BIGINT NOT NULL REFERENCES Physician(PhysicianID) ON DELETE CASCADE,
  Phone TEXT NOT NULL,
  Type TEXT
);
CREATE TABLE PhysicianSpecialty (
  PhysicianID BIGINT NOT NULL REFERENCES Physician(PhysicianID) ON DELETE CASCADE,
  Specialty TEXT NOT NULL,
  PRIMARY KEY (PhysicianID, Specialty)
);
CREATE TABLE Clinic (
  ClinicCode VARCHAR(20) PRIMARY KEY,
  Name TEXT NOT NULL,
  Floor TEXT
);
CREATE TABLE Room (
  ClinicCode VARCHAR(20) NOT NULL REFERENCES Clinic(ClinicCode) ON DELETE CASCADE,
  RoomNumber VARCHAR(20) NOT NULL,
  PRIMARY KEY (ClinicCode, RoomNumber)
);
CREATE TABLE PhysicianOffice (
  PhysicianID BIGINT NOT NULL REFERENCES Physician(PhysicianID) ON DELETE CASCADE,
  ClinicCode VARCHAR(20) NOT NULL,
  RoomNumber VARCHAR(20) NOT NULL,
  PRIMARY KEY (PhysicianID, ClinicCode, RoomNumber),
  FOREIGN KEY (ClinicCode, RoomNumber) REFERENCES Room(ClinicCode, RoomNumber) ON DELETE CASCADE
);
CREATE TABLE Appointment (
  AppointmentID BIGSERIAL PRIMARY KEY,
  PatientNo BIGINT NOT NULL REFERENCES Patient(PatientNo) ON DELETE CASCADE,
  PhysicianID BIGINT NOT NULL REFERENCES Physician(PhysicianID) ON DELETE RESTRICT,
  ApptDateTime TIMESTAMP NOT NULL,
  Reason TEXT,
  DurationMin INT CHECK (DurationMin > 0),
  Notes TEXT
);
CREATE TABLE Treatment (
  TreatmentID BIGSERIAL PRIMARY KEY,
  AppointmentID BIGINT NOT NULL REFERENCES Appointment(AppointmentID) ON DELETE CASCADE,
  ProcedureCode TEXT,
  Cost NUMERIC(10,2) CHECK (Cost >= 0),
  FollowUpInstructions TEXT
);

