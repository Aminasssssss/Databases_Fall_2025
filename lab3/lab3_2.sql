DROP TABLE IF EXISTS Supplier CASCADE;
DROP TABLE IF EXISTS Category CASCADE;
DROP TABLE IF EXISTS Product CASCADE;
DROP TABLE IF EXISTS CustomerAddress CASCADE;
DROP TABLE IF EXISTS Customer CASCADE;
CREATE TABLE Customer (
  CustomerID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  Email CITEXT UNIQUE NOT NULL,
  BillingAddressLine TEXT,
  BillingCity TEXT,
  BillingRegion TEXT,
  BillingZip TEXT
);
CREATE TABLE CustomerAddress (
  AddressID BIGSERIAL PRIMARY KEY,
  CustomerID BIGINT NOT NULL REFERENCES Customer(CustomerID) ON DELETE CASCADE,
  AddressLine TEXT NOT NULL,
  City TEXT NOT NULL,
  Region TEXT,
  Zip TEXT,
  Type TEXT CHECK (Type IN ('shipping','other'))
);
CREATE TABLE "Order" (
  OrderID BIGSERIAL PRIMARY KEY,
  CustomerID BIGINT NOT NULL REFERENCES Customer(CustomerID) ON DELETE RESTRICT,
  OrderDate TIMESTAMP NOT NULL DEFAULT now(),
  Status TEXT,
  ShipToAddressID BIGINT REFERENCES CustomerAddress(AddressID) ON DELETE SET NULL
);
CREATE TABLE Product (
  ProductID BIGSERIAL PRIMARY KEY,
  SKU TEXT UNIQUE NOT NULL,
  Name TEXT NOT NULL,
  Description TEXT
);
CREATE TABLE Category (
  CategoryID BIGSERIAL PRIMARY KEY,
  Name TEXT UNIQUE NOT NULL
);
CREATE TABLE ProductCategory (
  ProductID BIGINT NOT NULL REFERENCES Product(ProductID) ON DELETE CASCADE,
  CategoryID BIGINT NOT NULL REFERENCES Category(CategoryID) ON DELETE CASCADE,
  PRIMARY KEY (ProductID, CategoryID)
);
CREATE TABLE Supplier (
  SupplierID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  ContactEmail CITEXT
);
CREATE TABLE ProductSupplier (
  ProductID BIGINT NOT NULL REFERENCES Product(ProductID) ON DELETE CASCADE,
  SupplierID BIGINT NOT NULL REFERENCES Supplier(SupplierID) ON DELETE CASCADE,
  PRIMARY KEY (ProductID, SupplierID)
);
CREATE TABLE Warehouse (
  WarehouseID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  AddressLine TEXT,
  City TEXT,
  Region TEXT,
  Zip TEXT
);
CREATE TABLE Inventory (
  WarehouseID BIGINT NOT NULL REFERENCES Warehouse(WarehouseID) ON DELETE CASCADE,
  ProductID BIGINT NOT NULL REFERENCES Product(ProductID) ON DELETE CASCADE,
  OnHandQty INT NOT NULL CHECK (OnHandQty >= 0),
  PRIMARY KEY (WarehouseID, ProductID)
);
CREATE TABLE Review (
  ReviewID BIGSERIAL PRIMARY KEY,
  CustomerID BIGINT NOT NULL REFERENCES Customer(CustomerID) ON DELETE CASCADE,
  ProductID BIGINT NOT NULL REFERENCES Product(ProductID) ON DELETE CASCADE,
  OrderID BIGINT NOT NULL REFERENCES "Order"(OrderID) ON DELETE CASCADE,
  Rating INT NOT NULL CHECK (Rating BETWEEN 1 AND 5),
  ReviewText TEXT,
  ReviewDate DATE NOT NULL DEFAULT CURRENT_DATE,
  UNIQUE (CustomerID, ProductID, OrderID)
);
CREATE TABLE OrderItem (
  OrderID BIGINT NOT NULL REFERENCES "Order"(OrderID) ON DELETE CASCADE,
  LineNo INT NOT NULL,
  ProductID BIGINT NOT NULL REFERENCES Product(ProductID) ON DELETE RESTRICT,
  Qty INT NOT NULL CHECK (Qty > 0),
  UnitPriceAtPurchase NUMERIC(10,2) NOT NULL CHECK (UnitPriceAtPurchase >= 0),
  PRIMARY KEY (OrderID, LineNo)
);

