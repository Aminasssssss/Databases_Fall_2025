DROP TABLE IF EXISTS StudentOrg CASCADE;
CREATE TABLE StudentOrg (
  StudentID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  Email CITEXT UNIQUE NOT NULL,
  Major TEXT,
  Year INT CHECK (Year BETWEEN 1 AND 10)
);
CREATE TABLE Club (
  ClubID BIGSERIAL PRIMARY KEY,
  Name TEXT UNIQUE NOT NULL,
  Description TEXT
);
CREATE TABLE Membership (
  ClubID BIGINT NOT NULL REFERENCES Club(ClubID) ON DELETE CASCADE,
  StudentID BIGINT NOT NULL REFERENCES StudentOrg(StudentID) ON DELETE CASCADE,
  JoinedOn DATE DEFAULT CURRENT_DATE,
  RoleAtJoin TEXT,
  PRIMARY KEY (ClubID, StudentID)
);
CREATE TABLE Officer (
  ClubID BIGINT NOT NULL REFERENCES Club(ClubID) ON DELETE CASCADE,
  StudentID BIGINT NOT NULL REFERENCES StudentOrg(StudentID) ON DELETE CASCADE,
  Title TEXT NOT NULL,
  TermStart DATE NOT NULL,
  TermEnd DATE,
  PRIMARY KEY (ClubID, StudentID, TermStart)
);
CREATE TABLE FacultyOrg (
  FacultyID BIGSERIAL PRIMARY KEY,
  Name TEXT NOT NULL,
  Dept TEXT
);
CREATE TABLE ClubAdvisor (
  ClubID BIGINT PRIMARY KEY REFERENCES Club(ClubID) ON DELETE CASCADE,
  FacultyID BIGINT NOT NULL REFERENCES FacultyOrg(FacultyID) ON DELETE RESTRICT
);
CREATE TABLE Room (
  Building TEXT NOT NULL,
  RoomNo TEXT NOT NULL,
  Capacity INT CHECK (Capacity >= 0),
  PRIMARY KEY (Building, RoomNo)
);
CREATE TABLE Event (
  EventID BIGSERIAL PRIMARY KEY,
  ClubID BIGINT NOT NULL REFERENCES Club(ClubID) ON DELETE CASCADE,
  Title TEXT NOT NULL,
  StartsAt TIMESTAMP NOT NULL,
  EndsAt TIMESTAMP NOT NULL,
  Description TEXT
);
CREATE TABLE EventAttendee (
  EventID BIGINT NOT NULL REFERENCES Event(EventID) ON DELETE CASCADE,
  StudentID BIGINT NOT NULL REFERENCES StudentOrg(StudentID) ON DELETE CASCADE,
  RSVPStatus TEXT,
  PRIMARY KEY (EventID, StudentID)
);
CREATE TABLE RoomReservation (
  ReservationID BIGSERIAL PRIMARY KEY,
  EventID BIGINT NOT NULL REFERENCES Event(EventID) ON DELETE CASCADE,
  Building TEXT NOT NULL,
  RoomNo TEXT NOT NULL,
  StartDT TIMESTAMP NOT NULL,
  EndDT TIMESTAMP NOT NULL,
  ExpectedAttendance INT CHECK (ExpectedAttendance >= 0),
  FOREIGN KEY (Building, RoomNo) REFERENCES Room(Building, RoomNo) ON DELETE RESTRICT
);
CREATE TABLE Budget (
  ClubID BIGINT NOT NULL REFERENCES Club(ClubID) ON DELETE CASCADE,
  Year INT NOT NULL,
  Amount NUMERIC(12,2) NOT NULL CHECK (Amount >= 0),
  PRIMARY KEY (ClubID, Year)
);
CREATE TABLE Expense (
  ExpenseID BIGSERIAL PRIMARY KEY,
  ClubID BIGINT NOT NULL REFERENCES Club(ClubID) ON DELETE CASCADE,
  ExpenseDate DATE NOT NULL,
  Amount NUMERIC(12,2) NOT NULL CHECK (Amount >= 0),
  Purpose TEXT,
  ApprovedBy TEXT
);


