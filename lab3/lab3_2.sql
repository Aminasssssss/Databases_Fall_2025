CREATE EXTENSION IF NOT EXISTS citext;

DROP TABLE IF EXISTS staff CASCADE;
CREATE TABLE staff (
  StaffID BIGSERIAL PRIMARY KEY,
  NationalID VARCHAR(20) NOT NULL UNIQUE,
  WorkEmail CITEXT NOT NULL UNIQUE,
  Mobile VARCHAR(30) NOT NULL UNIQUE,
  FullName TEXT NOT NULL,
  Unit TEXT,
  AnnualSalary NUMERIC(12,2) CHECK (AnnualSalary >= 0)
);

DROP TABLE IF EXISTS enrollment CASCADE;
DROP TABLE IF EXISTS group_offering CASCADE;
CREATE TABLE group_offering (
  SubjectCode VARCHAR(20) NOT NULL,
  GroupNo VARCHAR(10) NOT NULL,
  Term VARCHAR(10) NOT NULL,
  Year INT NOT NULL CHECK (Year >= 1900),
  CreditHours INT NOT NULL CHECK (CreditHours > 0),
  PRIMARY KEY (SubjectCode, GroupNo, Term, Year)
);
CREATE TABLE enrollment (
  StudentNo BIGINT NOT NULL,
  SubjectCode VARCHAR(20) NOT NULL,
  GroupNo VARCHAR(10) NOT NULL,
  Term VARCHAR(10) NOT NULL,
  Year INT NOT NULL,
  Grade VARCHAR(2),
  PRIMARY KEY (StudentNo, SubjectCode, GroupNo, Term, Year),
  FOREIGN KEY (SubjectCode, GroupNo, Term, Year)
    REFERENCES group_offering (SubjectCode, GroupNo, Term, Year)
    ON DELETE RESTRICT
);
CREATE INDEX IF NOT EXISTS enrollment_student_idx ON enrollment (StudentNo);

DROP TABLE IF EXISTS registration CASCADE;
DROP TABLE IF EXISTS module CASCADE;
DROP TABLE IF EXISTS lecturer CASCADE;
DROP TABLE IF EXISTS student CASCADE;
DROP TABLE IF EXISTS faculty CASCADE;
CREATE TABLE faculty (
  DeptCode VARCHAR(10) PRIMARY KEY,
  DeptName TEXT NOT NULL,
  DeanID BIGINT
);
CREATE TABLE lecturer (
  LecturerID BIGSERIAL PRIMARY KEY,
  FullName TEXT NOT NULL,
  DeptCode VARCHAR(10),
  Title TEXT,
  FOREIGN KEY (DeptCode) REFERENCES faculty(DeptCode) ON DELETE SET NULL
);
CREATE TABLE module (
  ModuleID BIGSERIAL PRIMARY KEY,
  Title TEXT NOT NULL,
  Credits INT NOT NULL CHECK (Credits > 0),
  DeptCode VARCHAR(10),
  FOREIGN KEY (DeptCode) REFERENCES faculty(DeptCode) ON DELETE RESTRICT
);
CREATE TABLE student (
  StudentNo BIGSERIAL PRIMARY KEY,
  FullName TEXT NOT NULL,
  Email CITEXT UNIQUE,
  MajorCode VARCHAR(10),
  AcademicAdvisorID BIGINT,
  FOREIGN KEY (MajorCode) REFERENCES faculty(DeptCode) ON DELETE SET NULL,
  FOREIGN KEY (AcademicAdvisorID) REFERENCES lecturer(LecturerID) ON DELETE SET NULL
);
CREATE TABLE registration (
  StudentNo BIGINT NOT NULL,
  ModuleID BIGINT NOT NULL,
  Term VARCHAR(10) NOT NULL,
  Year INT NOT NULL CHECK (Year >= 1900),
  Status TEXT,
  PRIMARY KEY (StudentNo, ModuleID, Term, Year),
  FOREIGN KEY (StudentNo) REFERENCES student(StudentNo) ON DELETE CASCADE,
  FOREIGN KEY (ModuleID) REFERENCES module(ModuleID) ON DELETE RESTRICT
);

